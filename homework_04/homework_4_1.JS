let num = 21;
let sumOfDividers = 0;
for (let i = 1; i <= num; i++) {
  if (num % i === 0) { // проверка на делитель
    sumOfDividers += i; // если true то суммируем вместе с переменной что отвечает за сумму делителей
  } else { // если иначе то продолжаем выполнять программу цикла
    continue;
  };
  for (let j = i; j <= num; j++){ // позже выполняется встроенный цикл , что отвечает за сравнение суммы делителей что вышли из программы с суммой делителей свойственными простому числу
    if (sumOfDividers > num + 1) { // проверка если сумма делителей превысила сумму делителей простого числа то можно не продолжать выполнение программы и подвести итог что число не простое
      console.log("Число N не є простим числом");
      break;
    } else if (sumOfDividers === num + 1) { // если же сумма делителей в сравнении с свойством простого числа true то число простое
      console.log("Число N є простим числом");
      break;
    } else {
      continue; // если ничто из этого не соответствует, то программа выполняется еще раз но к переменной i добавляется единица.
    };
  };
};


